#!/usr/bin/env python3
"""Independent verification of HW/AG classification for CapBench tasks.

Usage: python3 verify_classification.py T01 T02 T03 ...

For each task, independently classifies every file in gt_files.txt as HW or AG
by reading file headers and checking path patterns, then compares with the
current classification in handwritten_files.txt / auto_generated_files.txt.

Reports discrepancies.
"""

import os
import sys
import fnmatch

BASE = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "base_repo")

# --- Classification rules ---

AG_BASENAME_PATTERNS = [
    "*.pb.go", "*.pb.cc", "*.pb.h", "*_pb2.py", "*_pb2_grpc.py",
    "*.gen.go", "zz_generated_*.go", "zz_generated*.go", "bindata.go",
    "*.swagger.json", "swagger.json",
    "package-lock.json", "yarn.lock", "Cargo.lock", "Gemfile.lock",
    "go.sum", "*_generated.go", "*.generated.ts",
    "*.snap", "*.golden",
]

AG_PATH_CONTAINS = [
    "vendor/", "node_modules/", "third_party/",
    "__snapshots__/",
    "tests/baselines/reference/",
]

AG_HEADER_MARKERS = [
    "do not edit",
    "// code generated",
    "auto-generated",
    "autogenerated",
    "@generated",
    "this file was autogenerated",
    "this file is generated",
    "this file is auto-generated",
    "automatically generated",
    "generated by gnu autoconf",
    "generated by tools/",
]

GENERATOR_SCRIPT_EXCLUSIONS = [
    "this script generates",
    "generate c code from",
    "generates the opcode",
    "generates token",
    "this tool generates",
]


def classify_file(filepath: str, repo_dir: str) -> tuple[str, str]:
    """Return ('AG'|'HW', reason)."""
    basename = os.path.basename(filepath)

    for pat in AG_BASENAME_PATTERNS:
        if fnmatch.fnmatch(basename, pat):
            return "AG", f"basename:{pat}"

    for pat in AG_PATH_CONTAINS:
        if pat in filepath:
            return "AG", f"path:{pat}"

    full = os.path.join(repo_dir, filepath)
    if os.path.isfile(full):
        try:
            with open(full, "r", errors="ignore") as f:
                header = f.read(1000).lower()

            for excl in GENERATOR_SCRIPT_EXCLUSIONS:
                if excl in header:
                    return "HW", "generator_script"

            for marker in AG_HEADER_MARKERS:
                if marker in header:
                    return "AG", f"header:{marker}"
        except Exception:
            pass

    return "HW", "default"


def verify_task(task: str) -> dict:
    """Verify one task. Returns dict with results."""
    eval_dir = os.path.join(BASE, task, "eval")
    repo_dir = os.path.join(BASE, task, "repo")

    gt_f = os.path.join(eval_dir, "gt_files.txt")
    hw_f = os.path.join(eval_dir, "handwritten_files.txt")
    ag_f = os.path.join(eval_dir, "auto_generated_files.txt")

    if not all(os.path.exists(f) for f in [gt_f, hw_f, ag_f]):
        return {"task": task, "error": "missing eval files"}

    gt = set(l.strip() for l in open(gt_f) if l.strip())
    current_hw = set(l.strip() for l in open(hw_f) if l.strip())
    current_ag = set(l.strip() for l in open(ag_f) if l.strip())

    # Structural check
    structural_ok = (current_hw | current_ag == gt) and not (current_hw & current_ag)

    # Independent classification
    my_hw = set()
    my_ag = set()
    ag_reasons = {}
    for fpath in gt:
        cls, reason = classify_file(fpath, repo_dir)
        if cls == "AG":
            my_ag.add(fpath)
            ag_reasons[fpath] = reason
        else:
            my_hw.add(fpath)

    # Compare
    should_be_ag = current_hw & my_ag  # in current HW but I say AG
    should_be_hw = current_ag & my_hw  # in current AG but I say HW
    agree_hw = current_hw & my_hw
    agree_ag = current_ag & my_ag

    return {
        "task": task,
        "gt_count": len(gt),
        "structural_ok": structural_ok,
        "current_hw": len(current_hw),
        "current_ag": len(current_ag),
        "my_hw": len(my_hw),
        "my_ag": len(my_ag),
        "agree_hw": len(agree_hw),
        "agree_ag": len(agree_ag),
        "should_be_ag": sorted(should_be_ag),
        "should_be_hw": sorted(should_be_hw),
        "ag_reasons": {f: ag_reasons.get(f, "?") for f in should_be_ag},
    }


def main():
    tasks = sys.argv[1:]
    if not tasks:
        print("Usage: python3 verify_classification.py T01 T02 ...")
        sys.exit(1)

    discrepancies = 0
    for task in sorted(tasks):
        r = verify_task(task)
        if "error" in r:
            print(f"{task}: ERROR - {r['error']}")
            continue

        ok = not r["should_be_ag"] and not r["should_be_hw"] and r["structural_ok"]
        if ok:
            print(f"{task}: OK (GT={r['gt_count']}, HW={r['current_hw']}, AG={r['current_ag']})")
        else:
            discrepancies += 1
            print(f"{task}: DISCREPANCY (GT={r['gt_count']})")
            if not r["structural_ok"]:
                print(f"  STRUCTURAL ERROR: HW+AG != GT or overlap")
            for f in r["should_be_ag"]:
                print(f"  HW->AG: {f}  [{r['ag_reasons'].get(f, '?')}]")
            for f in r["should_be_hw"]:
                print(f"  AG->HW: {f}")

    print(f"\n=== {len(tasks)} tasks checked, {discrepancies} with discrepancies ===")


if __name__ == "__main__":
    main()
